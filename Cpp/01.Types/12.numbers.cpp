//------------------------------12. Number Operations--------------------------//


/*
 *  use cmath library for math operations
 */

#include <iostream>
#include <cmath>

using std::cout;
using std::endl;

int main(){

    cout << "Using squareroot: " << sqrt(25.0) << endl;
    cout << "Using infinity: " << pow(-9, 999) << endl;
    cout << "Using nan: " << NAN << endl;
    cout << "Using modulus: " << 10%3 << endl;
    cout << "Using modulus: " << fmod(10, 3) << endl;
    cout << "Finding maximum: " << fmax(19, 6.7) << endl;
    cout << "Finding minimum: " << fmin(19, 6.7) << endl;
    cout << "Greatest integer: " << ceil(5.67) << endl;
    cout << "Smallest integer: " << floor(5.67) << endl;
    cout << "Rounding off: " << round(1.51) << endl;
}











































