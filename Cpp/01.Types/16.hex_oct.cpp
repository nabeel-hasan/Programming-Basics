 // ----------------16. Hexadecimal and Octal Number Conversion ------------------//
 

#include <iostream>

using std::cout;
using std::cin;
using std::endl;
using std::hex;
using std::oct;

int main(){
    int number;
    cout << "Enter a number: " ;
    cin >> number;
    cout << "The hex number is : " << hex << number << endl; 
    cout << "The oct number is : " << oct << number << endl; 
}




















